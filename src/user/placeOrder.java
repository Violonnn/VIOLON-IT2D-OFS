 public void displayData() {     
    try {
        dbConnector dbc = new dbConnector();
        session ss = session.getInstance();
        String userId = String.valueOf(ss.getUid());

        if (userId.isEmpty()) {
            System.out.println("Error: User ID is empty!");
            return;
        }

        String sql = "SELECT o.orderDate AS 'Order Date', " +
                     "o.orderID AS 'Order ID', " +
                     "o.customer AS 'Customer', " +
                     "o.address AS 'Address', " +
                     "o.phone AS 'Phone', " +
                     "SUM(oi.orderQuan) AS 'Total Items', " +
                     "FORMAT(SUM(oi.itemPrice * oi.orderQuan), 2) AS 'Total Amount' " +
                     "FROM tbl_order o " +
                     "JOIN tbl_order_items oi ON o.orderID = oi.orderID " +
                     "WHERE o.user_id = '" + userId + "' AND o.orderStats = 'Pending' " +
                     "GROUP BY o.orderID, o.customer, o.address, o.phone, o.orderDate";

        ResultSet rs = dbc.getData(sql);

        if (!rs.isBeforeFirst()) {
            System.out.println("No pending orders for user_id: " + userId);
        } else {
            usersTable.setModel(DbUtils.resultSetToTableModel(rs));

            // Add peso sign manually to the "Total Amount" column
            for (int i = 0; i < usersTable.getRowCount(); i++) {
                String amount = (String) usersTable.getValueAt(i, usersTable.getColumn("Total Amount").getModelIndex());
                usersTable.setValueAt("â‚±" + amount, i, usersTable.getColumn("Total Amount").getModelIndex());
            }
        }

        rs.close();
    } catch (SQLException ex) {
        System.out.println("SQL Error: " + ex.getMessage());
        ex.printStackTrace();
    }
    
     
}


